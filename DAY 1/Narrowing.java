class Narrowing{
    public static void main(String[] args) {
        int a =(int)243.23433;
        System.out.println(a);
        long l1=(long)234.234f;
        System.out.println(l1);

        //converting byte to char
        byte b1=70;
        char ch2=(char)b1;
        System.out.println(ch2);

        //converting short to char
        short s1=120;
        char ch3 =(char)s1;
        System.out.println(ch3);
        
    }
}
